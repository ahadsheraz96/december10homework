public class PracticeOfArrays {

    public static void main (String args []){
        String [] namesOfMyFriends= new String[5];

        namesOfMyFriends[0]="khan";
        namesOfMyFriends[1]="hassan";
        namesOfMyFriends[2]="zia";
        namesOfMyFriends[3]="waqar";
        namesOfMyFriends[4]="amir";

        System.out.println(namesOfMyFriends[0]);
        System.out.println(namesOfMyFriends[1]);
        System.out.println(namesOfMyFriends[2]);
        System.out.println(namesOfMyFriends[3]);
        System.out.println(namesOfMyFriends[4]);

        for (int x =0; x <=3; x++ ){
            System.out.println(namesOfMyFriends[x]);
        }

    }

}
